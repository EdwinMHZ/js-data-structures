/*

    What it's sorting?
    Sorting is the process of rearranging the
    items in a collection(e.g an array) so that
    the items are in some kind of order

    Examples
    - Sorting numbers from smallest to largest
    - Sorting names alphabetically
    - Sorting movies based on release year

    Built-in JavaScript Sorting
    - The built-in sorting method accepts an optional comparator function
    - You can use this comparator function to tell JavaScript how you want it to sort
    - The comparator looks at pairs of elements (a and b) determines their sort based on the return value
        - If returns a negative number, a should come before b
        - If returns a positive number, a should come after b
        - If returns 0, a and b are the same as far as the sort is concerned
*/